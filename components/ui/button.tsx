import * as React from "react";
import { motion } from "framer-motion";
import { cn } from "@/lib/utils";

export interface ButtonProps
  extends React.ButtonHTMLAttributes<HTMLButtonElement> {
  variant?:
    | "default"
    | "destructive"
    | "outline"
    | "secondary"
    | "ghost"
    | "link";
  size?: "default" | "sm" | "lg" | "icon";
  asChild?: boolean;
}

const Button = React.forwardRef<HTMLButtonElement, ButtonProps>(
  (
    {
      className,
      variant = "default",
      size = "default",
      asChild = false,
      ...props
    },
    ref
  ) => {
    const baseStyles =
      "inline-flex items-center justify-center gap-2 whitespace-nowrap rounded-lg text-sm font-semibold transition-all duration-200 focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-[#0a923c] focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50";

    const variants = {
      default:
        "bg-[#0a923c] text-white shadow-sm hover:bg-[#0d7a33] active:scale-[0.98]",
      destructive:
        "bg-red-600 text-white shadow-sm hover:bg-red-700 active:scale-[0.98]",
      outline:
        "border border-gray-300 bg-white text-gray-900 shadow-sm hover:bg-gray-50 hover:text-[#0a923c] active:scale-[0.98]",
      secondary:
        "bg-gray-100 text-gray-900 shadow-sm hover:bg-gray-200 active:scale-[0.98]",
      ghost:
        "text-gray-700 hover:bg-gray-100 hover:text-[#0a923c] active:scale-[0.98]",
      link: "text-[#0a923c] underline-offset-4 hover:underline",
    };

    const sizes = {
      default: "h-10 px-4 py-2",
      sm: "h-9 px-3 text-xs",
      lg: "h-11 px-8 text-base",
      icon: "h-10 w-10",
    };

    return (
      <motion.button
        whileTap={{ scale: 0.98 }}
        className={cn(
          baseStyles,
          variants[variant],
          sizes[size],
          className
        )}
        ref={ref}
        {...props}
      />
    );
  }
);
Button.displayName = "Button";

export { Button };

